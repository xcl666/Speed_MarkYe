void dip_switch_update_user(uint8_t index, bool active) {
  switch (index) {
    case 0:
      if(active) {
        toggle_led_mode(false);
      } else {
        toggle_led_mode(true);
      }
      break;
    case 1:
      if(active) {
        #ifdef AUDIO_ENABLE
          audio_off();
        #endif
      } else {
        #ifdef AUDIO_ENABLE
          audio_on();
        #endif
      }
      break;
  }
}

